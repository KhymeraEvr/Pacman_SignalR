function print_arrow_key(keyCodeNumber) {

    var key_arrow_or_other = document.getElementById('direction'),
        LEFT = 37,
        UP = 38,
        RIGHT = 39,
        DOWN = 40;
    var dir;
    switch (keyCodeNumber) {
        case LEFT:
            direction.innerHTML = 'LEFT';
            dir = "LEFT";
            break;
        case UP:
            direction.innerHTML = 'UP';
            dir = "UP";
            break;
        case RIGHT:
            direction.innerHTML = 'RIGHT';
            dir = "RIGHT";
            break;
        case DOWN:
            direction.innerHTML = 'DOWN';
            dir = "DOWN";
            break;
    }
    if (keyCodeNumber >= 13 && keyCodeNumber <= 40) {
        connection.invoke("GetInput", dir);
    }
}

