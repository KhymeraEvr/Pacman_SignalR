connection.on("DrawFood", (name, x, y) => {
    var canvas = this.document.getElementById("CoinCanvas");
    var coinContext = canvas.getContext('2d');
    coinContext.beginPath();
    switch (name) {
        case "cherry":
            coinContext.arc(x * 24 + 11, y * 24 + 11, 8, 0, 2 * Math.PI, false);
            coinContext.fillStyle = "#ce1043";
            break;
        case "coin":
            coinContext.arc(x * 24 + 11, y * 24 + 11, 5, 0, 2 * Math.PI, false);
            coinContext.fillStyle = "#ffe030";
            break;

    }
    coinContext.fill();
});

